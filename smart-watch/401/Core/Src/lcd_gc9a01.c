void LCD_Address_Set(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2)
{
    LCD_WriteReg(0x2A);
    LCD_WriteData16(x1);
    LCD_WriteData16(x2);

    LCD_WriteReg(0x2B);
    LCD_WriteData16(y1);
    LCD_WriteData16(y2);

    LCD_WriteReg(0x2C);
}

void LCD_SendPixels(uint16_t *data, uint32_t len)
{
    LCD_DC_H();
    LCD_CS_L();

    uint8_t *pData = (uint8_t *)data;
    HAL_SPI_Transmit(&hspi1, pData, len * 2, HAL_MAX_DELAY);

    LCD_CS_H();
}

void LCD_Clear(uint16_t color)
{
    uint16_t buf[LCD_W];
    for(int i = 0; i < LCD_W; i++) buf[i] = color;

    LCD_Address_Set(0, 0, LCD_W - 1, LCD_H - 1);

    LCD_DC_H();
    LCD_CS_L();

    for(int y = 0; y < LCD_H; y++)
    {
        HAL_SPI_Transmit(&hspi1, (uint8_t*)buf, LCD_W * 2, HAL_MAX_DELAY);
    }

    LCD_CS_H();
}

void LCD_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    LCD_Address_Set(x, y, x, y);
    LCD_WriteData16(color);
}

void LCD_Fill(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2, uint16_t color)
{
    uint32_t len = (x2 - x1 + 1) * (y2 - y1 + 1);
    LCD_Address_Set(x1, y1, x2, y2);

    LCD_DC_H();
    LCD_CS_L();

    uint8_t hi = color >> 8;
    uint8_t lo = color & 0xFF;
    uint8_t buf[2] = {hi, lo};

    for(uint32_t i = 0; i < len; i++)
    {
        HAL_SPI_Transmit(&hspi1, buf, 2, HAL_MAX_DELAY);
    }

    LCD_CS_H();
}

