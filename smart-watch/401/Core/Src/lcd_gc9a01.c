void LCD_ShowChar(uint16_t x, uint16_t y, char ch, uint16_t fc, uint16_t bc)
{
    uint8_t temp, t1, t;
    uint16_t y0 = y;
    ch = ch - ' ';
    const uint8_t *pfont = ASCII_8x16 + ch * 16;

    for(t = 0; t < 16; t++)
    {
        temp = pfont[t];
        for(t1 = 0; t1 < 8; t1++)
        {
            if(temp & 0x80)
                LCD_DrawPoint(x, y, fc);
            else
                LCD_DrawPoint(x, y, bc);
            temp <<= 1;
            y++;
            if((y - y0) == 16)
            {
                y = y0;
                x++;
                break;
            }
        }
    }
}

void LCD_ShowString(uint16_t x, uint16_t y, const char *str, uint16_t fc, uint16_t bc)
{
    while(*str)
    {
        if(x > LCD_W - 8) break;
        LCD_ShowChar(x, y, *str, fc, bc);
        x += 8;
        str++;
    }
}
